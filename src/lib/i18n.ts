export type TranslationKey = keyof typeof DICT['id'];

export function translate(lang: Language, key: TranslationKey, vars?: Record<string, string | number>): string {
  const table = DICT[lang] as Record<string, string>;
  let text = table[key] ?? (DICT.id as Record<string, string>)[key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      text = text.replace(`{${k}}`, String(v));
    }
  }
  return text;
}
